package ex03;

public class Example_07 {
		public static void main(String[] args) {
			/*
			 * 논리부정 연산자
			 * -boolean 값을 가지는 피연산자 앞에
			 * (!)를 붙여서 값을 반대로 바꾸는 역할
			 * 
			 * 즉) true => false,
			 * 	  false => true
			 * 
			 */
			boolean am_i_pig = true;
			System.out.println(!am_i_pig);
			System.out.println(am_i_pig);
		}
}
